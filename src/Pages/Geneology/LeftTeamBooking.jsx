import React, { useEffect, useState } from "react";
import { useAuth } from "@/context/AuthContext";
import axios from "axios";
import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import {
  Loader2,
  Users,
  MapPin,
  IndianRupee,
  Calendar,
  FileDown,
} from "lucide-react";
import {
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableHeader,
  TableRow,
} from "@/components/ui/table";
import { Alert, AlertDescription } from "@/components/ui/alert";
import { Button } from "@/components/ui/button";
import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog";
import { Label } from "@/components/ui/label";
import { FaUserFriends } from "react-icons/fa";
import { MdRealEstateAgent } from "react-icons/md";
import { BsFillCreditCard2BackFill } from "react-icons/bs";

const API_BASE_URL = "https://shreejeebackend.onrender.com/api/v1/plots";

const LeftTeamBookings = () => {
  const { user, loading: authLoading } = useAuth();
  const [bookings, setBookings] = useState([]);
  const [loading, setLoading] = useState(false);
  const [summary, setSummary] = useState(null);
  const [selectedPlot, setSelectedPlot] = useState(null);
  const [isModalOpen, setIsModalOpen] = useState(false);

  const apiClient = axios.create({
    baseURL: API_BASE_URL,
    headers: { Authorization: `Bearer ${localStorage.getItem("accessToken")}` },
  });

  const fetchLeftTeamBookings = async () => {
    setLoading(true);
    try {
      const { data } = await apiClient.get("/user/plots/team/left");
      if (data.success) {
        setBookings(data.data.plots || []);
        setSummary({
          total: data.data.total || 0,
          teamMembers: data.data.teamMembers || 0,
        });
      }
    } catch (err) {
      console.error("Error fetching left team bookings:", err);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    if (!authLoading && user) fetchLeftTeamBookings();
  }, [authLoading, user]);

  const formatDate = (dateString) =>
    dateString
      ? new Date(dateString).toLocaleDateString("en-IN", {
        day: "2-digit",
        month: "short",
        year: "numeric",
      })
      : "N/A";

  const formatCurrency = (amount) =>
    `Rs ${new Intl.NumberFormat("en-IN", {
      maximumFractionDigits: 0,
    }).format(amount || 0)}`;

  const handleShowPaymentDetails = (plot) => {
    setSelectedPlot(plot);
    setIsModalOpen(true);
  };

  const handleDownloadPDF = () => {
    if (!selectedPlot) return;

    const doc = new jsPDF("p", "mm", "a4");
    let y = 20;

    // üü¶ Header Section
    doc.setFillColor(33, 150, 243);
    doc.rect(0, 0, 210, 25, "F");
    doc.setTextColor(255, 255, 255);
    doc.setFontSize(18);
    doc.text("Plot Booking Receipt", 70, 17);
    doc.setFontSize(10);
    doc.text("Generated by Bharat Real Estate Pvt Ltd", 65, 23);

    doc.setTextColor(0, 0, 0);
    y += 15;

    // üßç Buyer Info
    doc.setFontSize(13);
    doc.setFont("helvetica", "bold");
    doc.text("Buyer Details", 14, y);
    y += 6;
    doc.setDrawColor(200);
    doc.line(14, y, 196, y);
    y += 6;
    doc.setFont("helvetica", "normal");
    doc.setFontSize(11);
    const buyer = selectedPlot.bookingDetails?.buyerId || {};
    const pInfo = buyer.personalInfo || {};
    doc.text(`Name: ${pInfo.firstName || ""} ${pInfo.lastName || ""}`, 14, y);
    doc.text(`Member ID: ${buyer.memberId || "N/A"}`, 120, y);
    y += 6;
    doc.text(`Email: ${buyer.email || "N/A"}`, 14, y);
    y += 10;

    // üìç Plot Info
    doc.setFont("helvetica", "bold");
    doc.text("Plot Details", 14, y);
    y += 6;
    doc.setDrawColor(200);
    doc.line(14, y, 196, y);
    y += 6;
    doc.setFont("helvetica", "normal");
    const plot = selectedPlot;
    doc.text(`Project: ${plot.siteLocation?.siteName || "N/A"}`, 14, y);
    doc.text(`Plot No: ${plot.plotNumber || "N/A"}`, 120, y);
    y += 6;
    doc.text(`Facing: ${plot.features?.facing || "N/A"}`, 14, y);
    doc.text(`Size: ${plot.size?.value} ${plot.size?.unit}`, 120, y);
    y += 6;
    doc.text(`Booking Date: ${formatDate(plot.bookingDetails?.bookingDate)}`, 14, y);
    y += 10;

    // üí∞ Price Info
    doc.setFont("helvetica", "bold");
    doc.text("Payment Summary", 14, y);
    y += 6;
    doc.setDrawColor(200);
    doc.line(14, y, 196, y);
    y += 6;
    doc.setFont("helvetica", "normal");
    const pricing = plot.pricing || {};
    const booking = plot.bookingDetails || {};
    const total = pricing.totalPrice || 0;
    const paid = booking.totalPaidAmount || 0;

    doc.text(`Base Rate: ${formatCurrency(pricing.basePrice)}`, 14, y);
    doc.text(`Development Charges: ${formatCurrency(pricing.developmentCharges)}`, 120, y);
    y += 6;
    doc.text(`Total Price: ${formatCurrency(total)}`, 14, y);
    doc.text(`Total Paid: ${formatCurrency(paid)}`, 120, y);
    y += 6;
    doc.text(`Remaining: ${formatCurrency(total - paid)}`, 14, y);
    y += 10;

    // üßæ Payment Schedule Table
    const paymentSchedule =
      plot.bookingDetails?.paymentSchedule?.map((p, i) => [
        i + 1,
        formatDate(p.paidDate),
        formatCurrency(p.amount),
        p.receiptNo || "N/A",
        p.paymentMode?.toUpperCase() || "CASH",
        p.transactionId || "N/A",
        formatDate(p.transactionDate),
        p.notes || "N/A",
      ]) || [];

    autoTable(doc, {
      startY: y,
      head: [
        [
          "S.No",
          "Payment Date",
          "Amount",
          "Receipt No",
          "Mode",
          "Txn ID",
          "Txn Date",
          "Notes",
        ],
      ],
      body: paymentSchedule,
      theme: "striped",
      styles: { fontSize: 9, cellPadding: 2 },
      headStyles: {
        fillColor: [33, 150, 243],
        textColor: [255, 255, 255],
        halign: "center",
      },
    });

    y = doc.lastAutoTable.finalY + 15;

    // ‚úÖ Total Box
    doc.setDrawColor(50);
    doc.rect(14, y, 182, 15);
    doc.setFontSize(12);
    doc.setFont("helvetica", "bold");
    doc.text(`Total Amount Paid: ${formatCurrency(paid)}`, 20, y + 10);
    y += 25;

    // ‚úçÔ∏è Footer / Signature
    doc.setFont("helvetica", "italic");
    doc.setFontSize(10);
    doc.text(
      "This is a computer-generated receipt. Thank you for your payment!",
      14,
      y
    );
    y += 10;
    doc.line(150, y, 196, y);
    doc.text("Authorized Signature", 155, y + 5);

    doc.save(`${selectedPlot.plotName || "plot"}_receipt.pdf`);
  };

  if (authLoading)
    return (
      <div className="flex justify-center py-20">
        <Loader2 className="animate-spin h-8 w-8 text-gray-500" />
      </div>
    );

  if (!user)
    return (
      <div className="text-center text-gray-600 mt-20">
        Please log in to view left team bookings.
      </div>
    );

  return (
    <div className="p-4 sm:p-6 space-y-6">
      <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-3">
        <div>
          <h1 className="text-2xl sm:text-3xl font-bold text-gray-800 flex items-center gap-2">
            <FaUserFriends className="text-blue-600" />
            Left Team Bookings
          </h1>
          <p className="text-gray-500 mt-1">
            View all plots booked by your left team members
          </p>
        </div>
        <Badge
          variant="outline"
          className="text-base px-4 py-2 bg-gray-50 border-gray-200"
        >
          <Users className="h-4 w-4 mr-2" />
          {summary?.teamMembers || 0} Team Members
        </Badge>
      </div>

      {summary && (
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4">
          <Card className="shadow-sm hover:shadow-md transition">
            <CardContent className="pt-6">
              <div className="flex items-center justify-between">
                <div>
                  <p className="text-sm font-medium text-gray-500">
                    Total Bookings
                  </p>
                  <p className="text-3xl font-bold text-gray-900">
                    {summary.total}
                  </p>
                </div>
                <div className="h-12 w-12 bg-blue-100 rounded-full flex items-center justify-center">
                  <MapPin className="h-6 w-6 text-blue-600" />
                </div>
              </div>
            </CardContent>
          </Card>

          <Card className="shadow-sm hover:shadow-md transition">
            <CardContent className="pt-6">
              <div className="flex items-center justify-between">
                <div>
                  <p className="text-sm font-medium text-gray-500">
                    Team Members
                  </p>
                  <p className="text-3xl font-bold text-gray-900">
                    {summary.teamMembers}
                  </p>
                </div>
                <div className="h-12 w-12 bg-green-100 rounded-full flex items-center justify-center">
                  <Users className="h-6 w-6 text-green-600" />
                </div>
              </div>
            </CardContent>
          </Card>

          <Card className="shadow-sm hover:shadow-md transition">
            <CardContent className="pt-6">
              <div className="flex items-center justify-between">
                <div>
                  <p className="text-sm font-medium text-gray-500">
                    Total Value
                  </p>
                  <p className="text-3xl font-bold text-gray-900">
                    {formatCurrency(
                      bookings.reduce(
                        (sum, plot) => sum + (plot.pricing?.totalPrice || 0),
                        0
                      )
                    )}
                  </p>
                </div>
                <div className="h-12 w-12 bg-purple-100 rounded-full flex items-center justify-center">
                  <IndianRupee className="h-6 w-6 text-purple-600" />
                </div>
              </div>
            </CardContent>
          </Card>
        </div>
      )}

      {/* Table & Modal are same */}
      {/* ---- your existing table & modal code below remains unchanged ---- */}
      {/* (no change needed there, only PDF layout is updated) */}

      <Card className="shadow-sm">
        <CardHeader>
          <CardTitle className="text-xl flex items-center gap-2">
            <MdRealEstateAgent className="text-blue-500" /> Booking Details
          </CardTitle>
        </CardHeader>
        <CardContent>
          {loading ? (
            <div className="flex justify-center py-16">
              <Loader2 className="animate-spin h-8 w-8 text-gray-500" />
            </div>
          ) : bookings.length === 0 ? (
            <Alert>
              <AlertDescription className="text-center py-8">
                No bookings found from left team members yet.
              </AlertDescription>
            </Alert>
          ) : (
            <div className="overflow-x-auto rounded-md border">
              <Table>
                <TableHeader>
                  <TableRow className="bg-gray-50">
                    <TableHead>#</TableHead>
                    <TableHead>Plot No.</TableHead>
                    <TableHead>Plot Name</TableHead>
                    <TableHead>Buyer</TableHead>
                    <TableHead>Member ID</TableHead>
                    <TableHead>Site</TableHead>
                    <TableHead>City</TableHead>
                    <TableHead>Size</TableHead>
                    <TableHead>Total</TableHead>
                    <TableHead>Token</TableHead>
                    <TableHead>Date</TableHead>
                    <TableHead>Status</TableHead>
                    <TableHead>Details</TableHead>
                  </TableRow>
                </TableHeader>
                <TableBody>
                  {bookings.map((plot, i) => (
                    <TableRow key={plot._id}>
                      <TableCell>{i + 1}</TableCell>
                      <TableCell>{plot.plotNumber}</TableCell>
                      <TableCell>{plot.plotName}</TableCell>
                      <TableCell>
                        <div>
                          <div className="font-medium">
                            {plot.bookingDetails?.buyerId?.personalInfo?.firstName}{" "}
                            {plot.bookingDetails?.buyerId?.personalInfo?.lastName}
                          </div>
                          <div className="text-xs text-gray-500">
                            {plot.bookingDetails?.buyerId?.email}
                          </div>
                        </div>
                      </TableCell>
                      <TableCell>
                        {plot.bookingDetails?.buyerId?.memberId}
                      </TableCell>
                      <TableCell>{plot.siteLocation?.siteName}</TableCell>
                      <TableCell>{plot.siteLocation?.address?.city}</TableCell>
                      <TableCell>
                        {plot.size?.value} {plot.size?.unit}
                      </TableCell>
                      <TableCell>
                        {formatCurrency(plot.pricing?.totalPrice || 0)}
                      </TableCell>
                      <TableCell>
                        {formatCurrency(plot.bookingDetails?.tokenAmount || 0)}
                      </TableCell>
                      <TableCell>
                        <div className="flex items-center text-sm">
                          <Calendar className="h-3 w-3 mr-1 text-gray-400" />
                          {formatDate(plot.bookingDetails?.bookingDate)}
                        </div>
                      </TableCell>
                      <TableCell>
                        <Badge
                          variant={
                            plot.status === "booked" ? "default" : "secondary"
                          }
                        >
                          {plot.status}
                        </Badge>
                      </TableCell>
                      <TableCell>
                        <Button
                          variant="link"
                          size="sm"
                          onClick={() => handleShowPaymentDetails(plot)}
                        >
                          View
                        </Button>
                      </TableCell>
                    </TableRow>
                  ))}
                </TableBody>
              </Table>
            </div>
          )}
        </CardContent>
      </Card>

      {/* Modal unchanged */}
      <Dialog open={isModalOpen} onOpenChange={setIsModalOpen}>
        <DialogContent className="max-w-5xl sm:max-w-6xl overflow-y-auto max-h-[90vh]">
          <DialogHeader className="flex justify-between items-center">
            <DialogTitle className="flex items-center gap-2">
              <BsFillCreditCard2BackFill className="text-blue-600" />
              Payment Details - {selectedPlot?.plotName || "Plot"}
            </DialogTitle>
            {selectedPlot && (
              <Button
                size="sm"
                className="flex items-center gap-2"
                onClick={handleDownloadPDF}
              >
                <FileDown className="h-4 w-4" /> Download Receipt
              </Button>
            )}
          </DialogHeader>
          {/* rest content unchanged */}
        </DialogContent>
      </Dialog>
    </div>
  );
};

export default LeftTeamBookings;
